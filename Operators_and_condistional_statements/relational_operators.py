"""Relational Operators:
Relational Operators are used to compare values.
Gives True or False
Operators are >,<,==,<=,>=,!="""

n1 = int(input())#integer type(5)
n2 = int(input())#integer type (10)
print(n1 > n2) # False
print(n1 < n2) #True
print(n1 == n2) # False
print(n1 <= n2) #True
print(n1 >= n2) # False
print(n1 != n2) #True

